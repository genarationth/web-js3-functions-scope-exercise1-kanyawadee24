// Exercise 1.1 Create function following below
function addNumbers(a, b) {
  return a + b;
}
console.log(addNumbers(20, 3));

function subtractNumbers(a, b) {
  return a - b;
}
console.log(subtractNumbers(20, 3));

function multiplyNumbers(a, b) {
  return a * b;
}
console.log(multiplyNumbers(20, 3));

function divideNumbers(a, b) {
  return a % b;
}
console.log(divideNumbers(20, 3));

